package gr.teicm.notepad.Model;

public class CustomNotes {

    private String name;
    private String date;

    public CustomNotes(String name, String date) {
        this.name = name;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
